/**
 * Пример
 *
 * Вычисление факториала
 */
fun factorial(n: Int): Double {
    var result = 1.0
    for (i in 1..n) {
        result *= i
    }
    return result
}

/**
 * Пример
 *
 * Проверка числа на простоту -- результат true, если число простое
 */
fun isPrime(n: Int): Boolean {
    if (n < 2) return false
    if (n == 2) return true
    if (n % 2 == 0) return false
    for (m in 3..sqrt(n.toDouble()).toInt() step 2) {
        if (n % m == 0) return false
    }
    return true
}

/**
 * Пример
 *
 * Проверка числа на совершенность -- результат true, если число совершенное
 */
fun isPerfect(n: Int): Boolean {
    var sum = 1
    for (m in 2..n / 2) {
        if (n % m > 0) continue
        sum += m
        if (sum > n) break
    }
    return sum == n
}

/**
 * Пример
 *
 * Найти число вхождений цифры m в число n
 */
fun digitCountInNumber(n: Int, m: Int): Int =
    when {
        n == m -> 1
        n < 10 -> 0
        else -> digitCountInNumber(n / 10, m) + digitCountInNumber(n % 10, m)
    }

/**
 * Вспомогательные функции
 */
fun getDivisors(n: Int): Set<Int> {
    val divs = mutableSetOf(1, n)

    for (i in 2..sqrt(n.toDouble()).toInt())
        if (n % i == 0) {
            divs.add(i)
            if (i != n / i) divs.add(n / i)
        }

    return divs
}

fun getCommon(m: Int, n: Int): Set<Int> =
    getDivisors(m) intersect getDivisors(n)

fun taylor(
    x: Double,
    eps: Double,
    fstElem: Double,
    nInitial: Int
): Double {
    var elem = fstElem
    var sum = elem
    var n = nInitial

    while (abs(elem) > eps) {
        elem *= -sqr(x % (2 * PI)) / (n * (n + 1))
        sum += elem
        n += 2
    }
    return sum
}

fun getSequenceDigit(
    n: Int,
    func: (i: Int) -> Int
): Int {
    var number = 1
    var count = 0
    var result = 0

    while (count < n) {
        result = func(number++)
        count += digitNumber(result)
    }

    return (result / 10.0.pow(count - n).toInt()) % 10
}

/**
 * Простая
 *
 * Найти количество цифр в заданном числе n.
 * Например, число 1 содержит 1 цифру, 456 -- 3 цифры, 65536 -- 5 цифр.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun digitNumber(n: Int): Int {
    var number = n
    var count = 0

    do {
        count++
        number /= 10
    } while (number != 0)

    return count
}

/**
 * Простая
 *
 * Найти число Фибоначчи из ряда 1, 1, 2, 3, 5, 8, 13, 21, ... с номером n.
 * Ряд Фибоначчи определён следующим образом: fib(1) = 1, fib(2) = 1, fib(n+2) = fib(n) + fib(n+1)
 */
fun fib(n: Int): Int {
    val fib = mutableListOf(0, 1)

    for (i in 2..n) {
        fib[0] += fib[1]
        fib.sort()
    }

    return fib[1]
}


/**
 * Простая
 *
 * Для заданных чисел m и n найти наименьшее общее кратное, то есть,
 * минимальное число k, которое делится и на m и на n без остатка
 */
fun lcm(m: Int, n: Int): Int {
    val gcd = getCommon(m, n).max()!!

    return m * n / gcd
}

